// Helper functions for data management
const DataStore = {
  getOpportunities() {
    const local = localStorage.getItem('ath_opportunities');
    if (local) {
      try {
        return JSON.parse(local);
      } catch (e) {
        console.error("Failed to parse local storage opportunities, resetting...", e);
      }
    }
    // Store preloaded if not already stored
    localStorage.setItem('ath_opportunities', JSON.stringify(PRELOADED_OPPORTUNITIES));
    return PRELOADED_OPPORTUNITIES;
  },

  saveOpportunity(opp) {
    // If no image is provided, assign one based on category
    if (!opp.image || opp.image.trim() === '') {
      const catKey = opp.category.toLowerCase();
      opp.image = CATEGORY_IMAGES[catKey] || CATEGORY_IMAGES["jobs"];
    }

    const opps = this.getOpportunities();
    opps.unshift(opp); // Add to the beginning of the array so it shows as latest
    localStorage.setItem('ath_opportunities', JSON.stringify(opps));
    return opps;
  },

  getCategories() {
    const opps = this.getOpportunities();
    // Dynamically calculate counts
    return CATEGORIES.map(cat => {
      const count = opps.filter(o => o.category.toLowerCase() === cat.name.toLowerCase()).length;
      return { ...cat, count };
    });
  },

  getFaqs() {
    return FAQS;
  }
};
